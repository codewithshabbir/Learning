// Unfamiliar operators mein kuch operators hain jo beginners ko samajhne mein mushkil ho sakte hain:

// ++ (increment): variable ki value ko 1 se barhata hai.
// -- (decrement): variable ki value ko 1 se ghatata hai.
// += (addition assignment): variable mein addition karke result ko wapas assign karta hai.
// -= (subtraction assignment): subtraction karke result assign karta hai.

// Example:

var num = 5;
num++; // num ab 6 ho gaya
num--; // num ab 5 ho gaya

var total = 10;
total += 5;  // total ab 15 hai
total -= 3;  // total ab 12 hai