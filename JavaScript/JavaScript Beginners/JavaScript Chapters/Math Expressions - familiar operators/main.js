// Math expressions mein aise symbols hote hain jo calculations karte hain. 

// Familiar operators include:
// + (addition)
// - (subtraction)
// * (multiplication)
// / (division)
// % (modulus, remainder after division)

// Example
var sum = 5 + 3;       // addition
var diff = 10 - 7;     // subtraction
var product = 4 * 2;   // multiplication
var quotient = 8 / 2;  // division
var remainder = 10 % 3; // modulus

console.log("Addition:" + sum);              //8
console.log("Subtaction:" + diff);            //3
console.log("Multiplication" + product);          //8
console.log("Division" + quotient);         //4
console.log("Modulus" + remainder);       //1