// JavaScript mein variable names kaafi flexible hote hain lekin kuch rules follow karna zaroori hai.

// Legal Variable Names:

// Shuruat letters ya underscore _ se honi chahiye.
// Sirf letters, numbers, ya underscores ho sakte hain.
// Reserved keywords nahi honi chahiye (e.g., var, function).

// Example:

var firstName = 'Ali';  // legal
var _age = 20;          // legal

console.log(firstName);     // Ali
console.log(_age);          // 20


// Illegal Variable Names:

// Numbers se shuru nahi ho sakte.
// Spaces nahi ho sakti hain.
// Keywords use nahi kar sakte.

// Example:

// var 123name;   // illegal
// var first name;  // illegal
// var var;       // illegal

